/*
HOOKS

https://reactjs.org/docs/hooks-reference.html

Los Hooks permiten usar estados y otras características de React en componentes funcionales en lugar de componentes de clase.

Syntax --> 'use' + [hook]

Tipos de hooks:
    - State
    - Effect
    - Context
    - Ref
    - Callback
    - Reducer
    - [Personalizados]

--------------------------------------------------------------------

REGLAS DE LOS HOOKS

    - Sólo se pueden llamar en el nivel superior. No llamar Hooks dentro de loops o funciones anidadas.

    - Sólo llamar a Hooks en desde componentes funcionales de React. No llamar Hooks desde funciones de JavaScript regular (excepto los Hooks personalizados).

    - Los Hooks no funcionan en componentes de Clase, ya que existen para suplantar a los mismos, y darle esa funcionalidad a los componentes funcionales.
*/
